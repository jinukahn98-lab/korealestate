def load_legal_dong_codes():
    """법정동코드 CSV 데이터를 딕셔너리로 로드"""
    reader = csv.DictReader(io.StringIO(LEGAL_DONG_DATA))
    codes = {}
    for row in reader:
        code = row['code']
        codes[code] = {
            'code': code,
            'region_1depth': row['region_1depth'],
            'region_2depth': row['region_2depth'],
            'region_3depth': row.get('region_3depth', ''),
            'full_name': f"{row['region_1depth']} {row['region_2depth']}{' ' + row['region_3depth'] if row.get('region_3depth') else ''}"
        }
    return codes


def search_region(keyword):
    """키워드로 지역 검색"""
    codes = load_legal_dong_codes()
    results = []
    for code, info in codes.items():
        if keyword in info['full_name']:
            results.append(info)
    return results


def get_region_name(code):
    """법정동코드로 지역명 반환"""
    codes = load_legal_dong_codes()
    if code in codes:
        return codes[code]['full_name']
    return code


def get_all_regions():
    """전체 지역 목록 반환"""
    codes = load_legal_dong_codes()
    return list(codes.values())


def get_cities():
    """시/도 목록 반환"""
    codes = load_legal_dong_codes()
    cities = sorted(set(c['region_1depth'] for c in codes.values()))
    return cities


def get_districts(city):
    """특정 시/도의 구/군 목록 반환"""
    codes = load_legal_dong_codes()
    districts = []
    for info in codes.values():
        if info['region_1depth'] == city:
            name = info['region_2depth']
            if info['region_3depth']:
                name = f"{info['region_2depth']} {info['region_3depth']}"
            if name not in [d['name'] for d in districts]:
                districts.append({'code': info['code'], 'name': name})
    return districts


def find_code_by_name(name):
    """지역명으로 법정동코드 찾기"""
    codes = load_legal_dong_codes()
    for code, info in codes.items():
        if name in info['full_name'] or name in info['region_2depth']:
            return code
    return None


def save_to_db():
    """법정동코드를 DB에 저장"""
    from data.database import get_conn
    codes = load_legal_dong_codes()
    conn = get_conn()
    cur = conn.cursor()
    count = 0
    for info in codes.values():
        try:
            cur.execute('''
                INSERT OR IGNORE INTO legal_dong_codes (code, region_1depth, region_2depth, region_3depth)
                VALUES (?, ?, ?, ?)
            ''', (info['code'], info['region_1depth'], info['region_2depth'], info.get('region_3depth', '')))
            if cur.rowcount > 0:
                count += 1
        except:
            pass
    conn.commit()
    conn.close()
    print(f"✅ 법정동코드 {count}개 DB 저장 완료")
